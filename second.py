class Computer:
    def __init__(self, cpu, gpu, ram_amount):
        self.cpu = cpu
        self.gpu = gpu
        self.ram_amount = ram_amount

def info(self):
    print(f'CPU: {self.cpu}')
    print(f'GPU: {self.gpu}')
    print(f'RAM: {self.ram_amount}')


